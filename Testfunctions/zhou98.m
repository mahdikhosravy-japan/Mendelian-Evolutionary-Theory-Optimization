function [y] = zhou98(X)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% ZHOU (1998) FUNCTION
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUT:
%
% xx = [x1, x2, ..., xd]
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

[s1,d] = size(X);
y = nan(s1,1);
for i = 1:s1
xx =X(i,:);

for ii = 1:d
    xxa(ii) = 10 .* (xx(ii)-1/3);
end

for ii = 1:d
    xxb(ii) = 10 .* (xx(ii)-2/3);
end

phi1 = (2*pi)^(-d/2) * exp(-0.5*(norm(xxa))^2);
phi2 = (2*pi)^(-d/2) * exp(-0.5*(norm(xxb))^2);

y(i,1) = (10^d)/2 * (phi1 + phi2);

end
return
